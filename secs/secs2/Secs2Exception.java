package secs.secs2;

public class Secs2Exception extends Exception {

	private static final long serialVersionUID = 4357717366163333969L;

	public Secs2Exception() {
		super();
	}

	public Secs2Exception(String message) {
		super(message);
	}

	public Secs2Exception(Throwable cause) {
		super(cause);
	}

	public Secs2Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public Secs2Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
